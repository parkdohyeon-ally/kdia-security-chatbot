def _build_guide_metadata_text() -> str:
    """가이드 메타정보를 한 줄씩 압축 표기"""
    lines = []
    for version, meta in GUIDE_METADATA.items():
        if "미발간" in meta["full_title"] or "(" in meta["full_title"][:5]:
            continue
        current = " (현행)" if meta["is_current"] else ""
        lines.append(
            f"- {version}{current}: {meta['issued_date']} | {meta['scope']} | 분류축: {meta['structure_axis']}"
        )
    return "\n".join(lines)


